

#include <stdio.h>

int main()



{

    // int-> %d;

    int num = 24;

    printf("%d", num);

    // float-> %f;

    float decimalValue = 12.3;

    printf("\n%f", decimalValue);

    // char -> %c

    char character = 'a';

    printf("\n%c", character);

    // string ->%s

    char string[10] = "hello";

    printf("\n%s", string);

  
  
    return 0;
}
